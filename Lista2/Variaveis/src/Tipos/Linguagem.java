package Tipos;

public class Linguagem {
	
	public static void main (String args[]) {
		
		String a = "Bom Dia";
		byte b = 3;
		double c = 235.13;
		boolean  d = true;
		int e = -135;
		float f = 256.23F;
		char g = 'A';
		long h = 6463275245745L;
		
		System.out.println("A "+ a+
				"\nB "+ b+
				"\nC "+ c+
				"\nD "+ d+
				"\nE "+ e+
				"\nF "+ f+
				"\nG "+ g+
				"\nH "+ h);
		
	}

}
