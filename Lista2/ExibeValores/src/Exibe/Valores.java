package Exibe;

public class Valores {
	
	public static void main(String args[]) {
		
		byte bt = 19;
		short st = 1571;
		int it = 100000;
		long lg = 2147483648L;//PARA USAR O LONG DEVE POR O L NO FINAL
		float ft = 3.14F;//PARA USAR O FLOAT DEVE POR O F NO FINAL
		double db = 3.141592653589793;
		boolean bl = true;
		char ch = 'K';
		
		System.out.println("byte " + bt + 
		" \nshort " + st + 
		" \nint " + it + 
		" \nlong " + lg + 
		" \nfloat " + ft + 
		" \ndouble " + db +
		" \nboolean " + bl + 
		" \nchar " + ch);
		
		
		
	}

}
