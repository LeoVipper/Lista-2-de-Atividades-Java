package Tipos;

public class Primitivos {
	
	public static void main (String args[]) {
		
		byte bt = 50;
		short st = 30000;
		int it = 2000000;
		long lg = 30000000L;
		float ft = 3.15F;
		double db = 3.1568743215;
		char ch = 'H';
		
		System.out.println("byte " + bt + 
				"\nshort " + st + 
				"\nint " + it + 
				"\nlong " + lg + 
				"\nfloat " + ft + 
				"\ndouble " + db + 
				"\nchar " + ch);
		
		
	}

}
